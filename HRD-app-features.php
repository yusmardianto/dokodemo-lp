            <div class="fs-14 row justify-content-center">
                <div class="col-md-10">
                    <h3 class="margintop-80 mb-2 font-weight-bold">1. Online attendance feature</h3>
                    <p>Dokodemo-kerja features an online attendance function (e-attendance). So, through this application, employees can conduct their attendance digitally, meaning at any time and anywhere remotely. </p>
                    <p>In order to use this feature, employees simply need to have access to Dokodemo-Kerja already installed on their device (desktop or smartphone) and then press the "Play" button. The application will then record that as their hour of arrival, or the moment that employee starts working.</p>
                    <p>Through this feature, Dokodemo-Kerja can function as a suitable HRD application for managing remote employees who are employed through WFH strategies (Work From Home). In addition, this feature also makes it easier for the HRD team to find out who had started late that day.</p>
              
                    <h3 class="margintop-80 mb-2 font-weight-bold">2. Taking Screenshots while Employees Work</h3>
                    <p>Do you find it difficult to monitor your employees’ activities while at work? Don't worry, Dokodemo-Kerja provides a feature that enables the application to take screenshots through the employee's device screen.</p>
                    <p>Using this feature, HRD or management can accurately monitor the activities of their employees during working hours. The HRD team can thus find out if employees are actively working on their assigned tasks or spending their working hours doing other things outside of their duties, such as spending long hours on social media or watching Youtube, among other things.</p>
                    <p>Nevertheless, Dokodemo-Kerja still manages to maintain the privacy of its users. Therefore, any screenshots that are taken are low resolution snapshots; this is so that information pertaining to business communications or more personal info won’t be shown. In addition, this HRD application will also take screenshots randomly within a maximum time frame of 10 minutes.</p>

                    <h3 class="margintop-80 mb-2 font-weight-bold">3. Calculating Total Employee Work Hours</h3>
                    <p>Dokodemo-Kerja also provides a feature that monitors the total number of working hours of all personnel who use it. As we know, based on the labor regulations prescribed by law, the total number of working hours put in by any employee within one week totals up to 40 hours. This means that companies are allowed to use a 7-hour working day system for 6 working days or a system of 8 working hours within 5 working days.</p>
                    <p>Supporting the company’s ability to properly monitor the total number of working hours potentially put in by their employees, businesses can use Dokodemo-Kerja as a solution. When an employee presses the "Play" button, the system will start counting the employee's working hours and will eventually stop the count as soon as the employee presses the "Stop" button. Furthermore, the Dokodemo-kerja application is programmed in such a way that both the employee as well as the HRD team can see how many total working hours have been carried out on a daily, weekly, or monthly basis.</p>
                    <p>Through this feature, the HRD team can also find out who has not fulfilled the required weekly working hours (40 hours), also finding out which employees have been working overtime. This way, the HRD team is not only able to monitor the total number of working hours, but is also able to improve the workforce’s overall attention to discipline.</p>
     
                    <h3 class="margintop-80 mb-2 font-weight-bold">4. Requesting for a Leave Day or Other Permissions is Easy</h3>
                    <p>The Dokodemo-Kerja HRD application provides features that make requesting for various permissions to HR an easier task. The system shows how many leave quotas are available  and employees can easily apply for leave directly from the system without having to make complicated hardcopy documents.</p>
                    <p>Through this system, Dokodemo-kerja provides various leave application options that can be chosen by employees. Some of them include sick leave, paid leave, unpaid leave and government leave options, among others.</p>

                    <h3 class="margintop-80 mb-2 font-weight-bold">5. GPS tracking</h3>
                    <p>Dokodemo-Kerja has a GPS tracking feature. So, apart from being able to monitor employees working in the office, Dokodemo-Kerja also allows you to monitor employees working on a mobile basis. By installing this HRD application on a smartphone device, you can still monitor employee activities outside the office properly.</p>

                    <p class="margintop-80 mb-5">If the company can make good use of the various features above, the company can be helped in increasing the productivity of its employees. In addition, this application also allows employees to work flexibly and manage their working hours freely. Thus, employees will also have a good Work Life Balance without disturbing their productivity at work.</p> 
