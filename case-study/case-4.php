        <div class="container">
            <h1 class="text-center mb-5">Dokodemo-Kerja Story 4: <br>The Evolution of Logique’s HR Rules since implementing Dokodemo-Kerja.</h1>
            <div class="case-study__story">
                <div class="row">
                    <div class="offset-md-1 col-md-10">
                        <section class="text-justify">

                            <div class="row">
                                <div class="col-lg-5 offset-lg-0 offset-md-3 col-md-6">
                                    <img src="/img/case-study/dokodemo-story-4-ghitta.jpg" alt="A Logique Employee" class="img-fluid w-100 mb-4">
                                </div>
                                <div class="col-lg-7">
                                    <p>Hello, my name is Ghitta. I started working as an HR staff member in 2017. I would like to take this opportunity to explain how to utilize “Dokodemo-Kerja” to help transition into new company policies to solve some HR problems.</p>
                                    
                                    <div class="mb-4">
                                        <h5 class="mb-1"><b>Year 2017</b></h5>
                                        <p>Previously, we had not implemented any special HR rules. We made several exceptions for some staff with special circumstances so they could work remotely. The majority of staff working in the head office in Jakarta were allowed to choose when to start working from between 8-10 AM, as long as they fulfilled an 8-hour work period per day, excluding a 1-hour break. On the other hand, the staff in Yogyakarta worked from 8 AM to 5 PM.</p>
                                    </div>
                                </div>
                            </div>
        
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Challenges</b></h5>
                                <p>The majority of our Jakarta staff start working from 10:00 AM, while staff in Yogyakarta work from 08:00 AM. To make this work, I have suggested that both sides start working at 10:00 AM, but the latter refused because they would rather leave early than start late.</p>
                                <p>Another notable problem that had occured in the Jakarta office was the lack of means to maintain productivity when HR and other high ranking staff, who usually left the office at 18:00 PM, were not present. Employee productivity would decrease significantly because, while some of them would begin praying, the rest of the staff who had already finished their job would sit around eating and chatting with those who haven’t. As for those who worked from home, we wouldn’t have known. The only thing I could have done was to wait until the deadline for a project was over or there was an actual delay, after which I could review their activities and implement minor changes based on it.</p>
                            </div>
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Improvements</b></h5>
                                <p>“Dokodemo-Kerja” was developed and introduced to solve any existing staff management problems. Eventually, we were able to visualize who was actually doing their job, when they did it, and how they were doing it.</p>
                                <p>After Dokodemo-Kerja was introduced, staff who tended to arrive late eventually adopted the “quick to arrive, early to leave” mindset. With Dokodemo-Kerja’s work hour tracking feature, they began to realize that their working patterns, including breaks, were ineffective, and that their actual work time was less than 8 hours, even if they work until 21:00 PM.</p>
                                <p>A lot of the staff would then work longer hours and take shorter breaks in order to leave earlier. Eventually, the majority of staff were able to effectively sync their starting time with the time that they would leave.</p>
                                <p>We were then able to accurately evaluate the remote workers too. We found out that up until now, the reason for their subpar results was due to their inconsistent work schedules. This results in a high number of unfinished tasks.</p>
                                <p>Despite the apparent improvement after using Dokodemo-Kerja, we still needed an individual to regularly check the performance records of all staff members. For that, we assigned a member of our HR staff to correctly evaluate the data received.</p>
                                <ol class="story__list">Here are the four monitoring rules we set for Dokodemo-Kerja:
                                    <li>If 3 identical screenshot images are taken consecutively, the assigned HR staff will confirm with the employee involved. For those in our main branch, we would question them directly; as for those working in our other branches, we would contact them by using online communication tools.</li>
                                    <li>If an employee hasn’t started working by 10:00 AM, despite already arriving at the office, HR staff can approach them to confirm their reason for doing so. As for employees outside of the office, HR staff can contact them by using communication software.</li>
                                    <li>A lot of employees tend to forget to press the button to pause the recording of their work hours during their lunch break, which is a major reason to constantly check the monitoring records.</li>
                                    <li>If the screenshot result shows that the employees are constantly opening windows such as youtube, games, or other social media platforms on their screen, HR staff will remind them to stop such programs not related to their work.</li>
                                </ol>
                                <p>Additionally, we have also posted a sign on the prayer room door to remind employees to pause their Dokodemo-Kerja, and not to forget to turn it on again when going back to work, or to stop the application when finishing work.</p>
                            </div>
        
                            <div class="row">
                                <div class="col-lg-7">
                                    <div class="">
                                        <h5 class="mb-1"><b>Challenges (mid-2018)</b></h5>
                                        <p>Even though most of our employees have changed their working styles to conform to the new policy, some of them chose to ignore it and when confronted, would reply condescendingly. </p>
                                        <p>It would have been concerning if this was the case regarding our more productive employees, but in our company, the ones who have responded negatively are those with generally lower performance. Thus, I decided to employ a stricter standard of evaluation, which resulted in some personnel changes.</p>
                                    </div>
                                </div>
                                <div class="col-lg-5 offset-lg-0 offset-sm-2 col-sm-8 mb-lg-0 mb-3">
                                    <img src="/img/case-study/dokodemo-story-4.2-ghitta.png" alt="Logique Employees" class="img-fluid w-100">
                                </div>
                            </div>

                            <div class="mb-4">
                                <p>Furthermore, another problem arose within the organization. </p>
                                <p>Since implementing Dokodemo-Kerja, our employees have been able to conform to the 8 hours/day policy and have made sure to reach the minimum time requirement. However, when employees have little to no tasks in a day, they end up staring at the Dokodemo-Kerja window, waiting for the time counter to reach 8 hours. </p>
                            </div>
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Improvements</b></h5>
                                <p>From the company’s perspective, it is better for our employees to work flexibly when managing their work schedules. Employees are only expected to work for 8 hours or longer when they have urgent or impending tasks. The work policy was then changed to a <b>“40-hour per week, 6-hour per day minimum”</b> policy. Additionally, the core working time was set from 10:00 to 16:00. I personally would have preferred to shorten the core working time, but considering the team’s morale and management costs, we decided on 6 hours per day as the minimum.</p>
                                <p>We have also added several points to our SOP where staff are responsible to monitor each other to check whether their coworkers have started their Dokodemo-Kerja during the core time and if they have reached the 40-hour threshold that week.</p>
                                <p>The management staff who were usually only busy during the beginning and at the end of the month began to adopt the new policy into their schedules and would usually go home at 16:00 during the middle of the month. Importantly, employees who went to evening classes, or those who have to take care of their family during the morning and evening can still work efficiently without sacrificing their career.</p>
                            </div>
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Challenges (Beginning of 2019)</b></h5>
                                <p>The “40-hour per week” policy successfully increased productivity, but I began to think that there should be a way to promote an even more flexible work culture. We then introduced two more policies where we allowed all employees to work remotely, while shortening the core time even more. This was due to the following reasons:</p>
                                <ol class="story__lit">
                                    <li>It is more beneficial for employees who live far away from the office. Usually, they would’ve had a hard time commuting long distances to get to the office, only to end up going home early anyway after only working for 6 hours in order to beat the traffic.</li>
                                    <li>Many employees expressed that after reaching the end of their core working time period and then leaving for home at 16:00, they found it hard to effectively use their time (In order to have more time in the evening for their leisure, they need to leave the office a little bit earlier).</li>
                                    <li>To fulfill the “40-hour per week” requirement, there were several cases where some employees would work during the weekends. This is not an effective working strategy.</li>
                                </ol>
                            </div>
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Improvements</b></h5>
                                <p>At first, we allowed nearly everyone to work remotely. However, the days in a week for remote working was limited and would be determined by the type of work they did and their situation. We allowed each staff to choose a particular day in a week to work remotely.</p>
                                <p>The GA and HR staff, however, would not be allowed to work remotely so as to not lower the team’s morale. Only those who have received permission are allowed to work remotely. For example: “Employee A gets Tuesday, and Employee B gets Wednesday and Friday.”</p>
                                <p>There are several exceptions to the remote working schedule if it conflicts with a client or partner meeting. In which case, the supposed remote working day will turn into an office day with a shorter core time period, from 10:00 to 15:00. All these are regularly discussed in a weekly meeting so as to maintain the best remote working schedule for each employee.</p>
                                <p>The introduction of the remote working system to all employees significantly increased productivity. Employees who experience daily burnout on their long commutes could then effectively utilize their time to work during the 1-2 days of remote working per week.</p>
                                <p>Due to the normalization of remote working within the office environment, I became increasingly confident that it would be manageable, and thus I was able to increase the number of workers, particularly those who were able to work remotely full-time, i.e. people who lived far from the office or those living in remote areas.</p>
                            </div>
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Challenges and Improvements (mid-2019 - present)</b></h5>
                                <p>There has been a lot of positive feedback, and the number of employees working remotely has increased. The core working time has also been decreased to 4 hours. Despite the policy working as intended, there were some minor problems that required us to change some HR policies.</p>
                                <p>For example, if an employee was to start their work late, the HR team would have to waste additional time to get a confirmation from the person involved. To solve this, if the employees knew for sure that they would arrive later than 15 minutes from their starting time, they would have to notify everyone through Slack.</p>
                                <p>Additionally, due to the increase in phone calls and online meetings, employee PCs or Laptops would regularly fall into sleep/hibernation mode automatically. Dokodemo-Kerja does provide a time editing feature, but it has to be noted that this feature is not to be abused because not all editing requests will be accepted.</p>
                                <p>More detailed policy changes would be about not stopping the application during regular short toilet breaks. Specifically, if the short breaks were longer than 10 minutes, the user would have to stop the program.</p>
                                <p>The use of Dokodemo-Kerja is not only limited to internal usage by LOGIQUE, but is also used by our clients. Due to the various types of businesses that exist, we have provided extra services in adjusting the program so as to cater to the varied methods and processes of other companies that differ from LOGIQUE’s. One of the biggest changes that we made was to accommodate workers who are always on the road, and therefore would not have access to a laptop, or even have the need for one.</p>
                                <p>Thus, we developed a mobile application so that Dokodemo-Kerja could be used on a smartphone. It is now possible to know when and where (through GPS) our sales staff are during work hours.</p>
                                <p>Starting from March 2020, a full-time WFH policy was established, which caused a lack of opportunity for this mobile application to be used effectively. However, I will continue to change the PDCA cycle and further optimize our HR regulations.</p>
                            </div>
        
