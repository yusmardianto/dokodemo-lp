                                <div class="col-12">
                                    <p>LOGIQUE has recently made an official release of the new Dokodemo-Kerja version 2.1.3. The application comes with various exiting new features, such as the ability to easily manage employee tardiness, attendance history, and leave requests. Through these features, HR staff can thoroughly manage employee attendance effectively, with the data offered by the application also proving a useful asset in assessing the discipline and overall performance of employees.</p>
                                    <p>In this article, we aim to give you an overview of all the major features presented in the new Dokodemo-Kerja version 2.1.3.</p>
                                    <div class="mb-4">
                                        <h5 class="mb-1"><b>A Brief Overview of Dokodemo-Kerja</b></h5>
                                        <p>Dokodemo-Kerja is among the many HR tools developed by LOGIQUE DIGITAL INDONESIA. In a nutshell, this application was designed to monitor employee productivity.</p>
                                        <p>Apart from that, Dokodemo-Kerja also functions as an attendance tracking application. In contrast to the conventional means of attendance, where a magnetic card machine or fingerprint attendance system is used, attendance is tracked through Dokodemo-Kerja through much simpler means: employees need only to log into the system and press the play button. Indeed, this system is very quick and easy to use.</p>
                                        <p>Dokodemo-Kerja also runs on both mobile and desktop devices. In this regard, employees can work on a remote basis efficiently, not having to be present at any physically based office location. The system can also track how many hours every employee has worked on any given day, making it easy to know if an employee has actually put in the required number of hours. For this reason, Dokodemo-Kerja has become a highly recommended asset for those being employed on a remote working basis.</p>
                                    </div>
                                </div>
                            </div>
        
        
                            <div class="mb-4">
                                <h5 class="mb-1"><b>What are the new features of Dokodemo-Kerja?</b></h5>
                                <ol>
                                    <li>
                                        <h5 class="feature-title"><a target="_blank" rel=”noreferrer” href="/help#attendance_2" class="new-feature-link">Attendance History</a></h5>
                                        <p>This feature is useful for showing a list of employee attendance for one month or one year. In it, you can see the number of days off, leave, sickness, or other day offs.</p>
                                    </li>
                                    <li>
                                        <h5 class="feature-title"><a target="_blank" rel=”noreferrer” href="/help#attendance_3" class="new-feature-link">Delay</a></h5>
                                        <p>Every company sets its own standards in regards to working hours and set times. Appropriately, this new Dokodemo-Kerja feature will make it easier for HR staff to identify employees who, on occasion, do not meet these standards when coming late to work.</p>
                                    </li>
                                    <li>
                                        <h5 class="feature-title"><a target="_blank" rel=”noreferrer” href="/help#attendance_1" class="new-feature-link">Requesting a Leave or Day Off</a></h5>
                                        <p>Conventionally, applying for leave from the office can be lengthy process. Employees must fill out a form, send it to their supervisor, and then ask for a stamp and signature for an approval. Employees must also report their approval for their leave requests to HR staff so that the information can be updated via the employee database.</p>
                                    </li>
                                </ol>
                            </div>
                            <div class="mb-4">
                                <h5 class="mb-2"><b>How does LOGIQUE utilize Dokodemo-Kerja’s new features?</b></h5>
                                <ol>
                                    <li>
                                        <h5 class="feature-title">To monitor employees work habits and patterns</h5>
                                        <p>Through Dokodemo-Kerja, LOGIQUE’s HR staff can find out the specifics of each employee’s work habits and patters of work. This includes information about how long they have rested, how many hours they put into each day, and so on. Such information is very useful for maintaining a healthy level of employee productivity. So, whenever there is a delay on a task, the HR staff can check how the work habits of each employee involved. If their methods are deemed ineffective, the HR team can ask the employees concerned to immediately make improvements.</p>
                                        <p>Additionally, this feature also makes it easier for HR staff to identify suspicious work patterns. An example of this is when an employee asks for sick leave on Monday and Friday without attaching a doctor’s letter. If this is a frequent occurrence, there is always the possibility that the employee has feigned sickness so as to enjoy a longer vacation time. Thus the HR team can immediately take action so as to make sure such occurrences are minimized in the future.</p>
                                    </li>
                                    <li>
                                        <h5 class="feature-title">View a list of employees who are either usually or rarely late.</h5>
                                        <p>Before using Dokodemo-Kerja, LOGIQUE’s HR staff had experienced difficulty monitoring employees who were regularly late for work. Sometimes, even though employees have already arrived at the office, some do not immediately begin working, rather, they indulge in personal activities, such as opening Facebook, watching videos on Youtube, etc.</p>
                                        <p>However, with the features offered by Dokodemo-Kerja, such problems can be resolved efficiently. HR staff can easily make note of who has or hasn’t began their work through an easy to read list of employees. HR can then choose to reprimand the employee, appropriately.</p>
                                        <p>In addition, this feature also makes it easier for HR staff to monitor an employee’s level of discipline through the consistent monitoring of employee tardiness and the reasons given. This feature serves its use as a tool for employee performance appraisal, as well.</p>
                                        <p>From the information given through the application, LOGIQUE’s HR staff can also deduce what the factors are that contribute to employee tardiness. If the reason that is often given relates to oversleeping or waking up late, LOGIQUE’s HR staff can assist in finding solutions, like adjusting the employee’s set time to after 10:00.</p>
                                    </li>
                                    <li>
                                        <h5 class="feature-title">Employees can easily find out any remaining leave days they have</h5>
                                        <p>
                                        Employees often forget how many leave days they have left. As a result, when they want to plan a leave of absence, employees will repeatedly ask HR for this information. Without the new Dokodemo-Kerja feature, it would usually take HR staff time to look into the issue and inform the employee concerned, especially if the company has many staff members in their employ.
                                        </p>
                                        <p>
                                        With Dokodemo-Kerja, LOGIQUE employees can independently check what their leave day quota is. They can also find out when their remaining leave day quota will expire so that they can make the best out of what they have. This thereby allows HR to focus on other more pressing matters.
                                        </p>
                                    </li>
                                    <li>
                                        <h5 class="feature-title">Two-level requirement for day off submissions, namely approval from Team Leader and HR / Management.</h5>
                                        <p>
                                        Before the “leave-day” feature existed, LOGIQUE employees had to apply for leave via an email sent to the Team Leader and all HR teams. This procedure sometimes did not work efficiently due to leave requests arriving along with other emails.
                                        </p>
                                        <p>
                                        As a result, towards the approaching proposed date, employees would not have received further information regarding their leave status. In addition, sometimes an employee’s proposed leave would be cancelled suddenly by HR due to it not fitting the criteria of approval by their respective manager.
                                        </p>
                                        <p>
                                        However, these problems can be resolved with Dokodemo-Kerja’s new feature. Currently, LOGIQUE employees can apply for leave through a much simpler process. Employees can apply for leave directly through the application without having to send emails to various people.
                                        </p>
                                        <p>
                                        The system-of-approval currently in use also benefits HR as much as it does the employees. Approvals are made among two levels. At the first level, the Team Leader carries out the initial approval process. After receiving permission from the Team Leader, then at the second level, the HR team will forward the leave request to the Manager. The HR team will also submit the decision of approval from the Team Leader to the Manager to consider. If the Manager gives his/her approval, the employee will be allowed to proceed with the scheduled date of leave. Because it the request passes two levels of approval, sudden cancellations can be avoided.
                                        </p>
                                    </li>
                                    <li>
                                        <h5 class="feature-title">Clear Delegation of Tasks</h5>
                                        <p>
                                        When employees take time off, sometimes tasks they are assigned to get neglected because there is no clear handover process. As a result, the project is rarely completed in a timely manner, which is detrimental to the company.
                                        </p>
                                        <p>
                                        However, through Dokodemo-Kerja, Team Leaders can use the “delegated to” feature, which is a feature that allows for the transfer of responsibilities. The Team Leader is chosen to be the person who can delegate the task because they have thorough knowledge of the team’s workload. Thus, HR can find out who will be responsible in handling the various tasks of employees who are on leave, thereby maintaining the team’s overall level of productivity.
                                        </p>
                                        <p>
                                        Overall, the new feature included within the latest version of Dokodemo-Kerja simplifies the leave application process greatly. HR staff can also easily obtain valid information on the attendance rate and tardiness of every employee, gaining knowledge about, for example, whether employees are working in the office or remotely. Thus, company management or HR staff can control the performance and productivity of their employees quickly and efficiently.
                                        </p>
                                        <p>
                                        This HR Management tool is a highly effective digital solution for companies that aim to maintain their employees’ levels of productivity. Are you Interested giving Dokodemo-Kerja a try? Contact LOGIQUE immediately!
                                        </p>
                                    </li>
                                </ol>
