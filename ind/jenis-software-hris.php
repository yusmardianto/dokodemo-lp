                    <p>Solusi HR memang sudah semakin berkembang dan menyediakan berbagai fitur di dalamnya. Berdasarkan fungsinya, software HRIS terbagi ke dalam beberapa jenis, yaitu:</p>

                    <h3 class="margintop-70 mb-4 font-weight-bold">1. Operational HRIS</h3>
                    <p>Sesuai dengan namanya, operational HRIS berfungsi untuk mengelola kebutuhan HR yang bersifat operasional. Jenis software HRIS ini akan membantu pihak manajer dalam pengambilan keputusan rutin dan berulang karena sistem mampu menyediakan berbagai data yang berkaitan sumber daya manusia yang mereka kelola. Secara garis besar, beberapa data yang disediakan seperti:</p>
                    <ul>
                        <li class="mt-3">
                            <b>Employee Information Systems</b> <br>
                            Data ini berisi kumpulan informasi mengenai profil karyawan seperti nama, alamat, jenis kelamin, kewarganegaraan, lama masa kerja, dan masih banyak lagi.
                        </li>
                        <li class="mt-3">
                            <b>Position Control Systems</b> <br>
                            Data-data yang berkaitan dengan posisi atau jabatan karyawan di suatu perusahaan. Pada umumnya, info-info yang disediakan oleh data base ini berguna untuk mengidentifikasi posisi apa saja yang kosong dan perlu untuk segera diisi.
                        </li>
                        <li class="mt-3">
                            <b>Applicant Selection and Placement Information Systems </b> <br>
                            Data-data yang disediakan akan dipergunakan oleh tim HR ketika proses seleksi kandidat untuk menemukan calon karyawan yang sesuai dengan apa dibutuhkan oleh perusahaan.
                        </li>
                        <li class="mt-3">
                            <b>Performance Management Information Systems </b> <br>
                            Data-data yang berkaitan dengan penilaian kinerja dan produktivitas karyawan. Data-data tersebut biasanya akan dipergunakan untuk promosi jabatan atau untuk memberhentikan karyawan.
                        </li>
                        <li class="mt-3">
                            <b>Government Reporting and Compliance Information Systems </b> <br>
                            Data-data yang berisi berbagai informasi untuk menjaga ketertiban administrasi agar tetap sesuai dengan peraturan pemerintah.
                        </li>
                    </ul>

                    <h3 class="margintop-100 mb-4 font-weight-bold">2. Tactical HRIS </h3>
                    <p>Tactical HRIS dipergunakan untuk membantu pihak manajer ketika membuat keputusan yang berkaitan dengan alokasi sumber daya. Jenis HRIS ini memiliki beberapa sub part di dalamnya, yaitu:</p>
                    <ul>
                        <li class="mt-3">
                            <b>Job Analysis and Design Information Systems</b> <br>
                            Berisi data-data yang berkaitan dengan tugas, spesifikasi, dan tanggung jawab karyawan.
                        </li>
                        <li class="mt-3">
                            <b>Recruiting Information Systems</b> <br>
                            Secara garis besar akan berisi data-data mengenai rencana rekrutmen untuk menentukan posisi yang akan diisi serta keterampilan yang dibutuhkan untuk posisi tersebut.
                        </li>
                        <li class="mt-3">
                            <b>Compensation and Benefits Information Systems</b> <br>
                            Sesuai dengan namanya, data yang disediakan berisi informasi untuk membuat keputusan terkait pemberian kompensasi dan tunjangan yang akan diperoleh karyawan.
                        </li>
                        <li class="mt-3">
                            <b>Employee Training and Development Systems</b> <br>
                            Informasi untuk membantu tim HR dalam mengarahkan pelatihan untuk karyawan yang tertarik atau karyawan yang akan memperoleh manfaat dari pelatihan tersebut.
                        </li>
                    </ul>

                    <h3 class="margintop-100 mb-4 font-weight-bold">3. Strategic HRIS</h3>
                    <p>Strategic HRIS adalah jenis software HRIS yang menyediakan data untuk pengambilan keputusan strategis. Biasanya strategic HRIS akan dipergunakan untuk mengelola negosiasi tenaga kerja, pengaturan tenaga kerja serta mendukung program HR tertentu.</p>
                    <p>Dengan menggunakan strategic HRIS, Anda dapat mengetahui bagaiaman gambaran umum mengenai perecanaan tenaga kerja dan aset kerja yang Anda miliki. Strategic HRIS mempunyai beberapa komponen di dalamnya yaitu:</p>
                    <ul>
                        <li class="mt-3">
                            <b>Information Systems Supporting Workforce Planning</b> <br>
                            Berisi informasi mengenai kuantitas dan kualitas tenaga kerja yang dimiliki oleh suatu perusahaan. Informasi tersebut dapat dipergunakan untuk perencanaan strategis jangka panjang seperti ketika perusahaan ingin memperluas target market baru, membuka cabang baru, dan lain-lain.
                        </li>
                        <li class="mt-3">
                            <b>Information Systems Supporting Labor Negotiations</b> <br>
                            Berisi berbagai informasi untuk mendukung proses negosiasi tentang tenaga kerja baik itu dengan pihak vendor, serikat pekerja, atau pihak yang lain.
                        </li>
                        <li class="mt-3">
                            <b>Specialized Human Resource Information Systems Software</b> <br>
                            Data-data yang mendukung untuk manajemen SDM.
                        </li>
                    </ul>

                    <h3 class="margintop-100 mb-4 font-weight-bold">4. Comprehensive HRIS</h3>
                    <p>Comprehensive HRIS menghasilkan database yang saling terintegrasi dan bertindak sebagai suatu tempat yang berisi berbagai informasi yang dapat digunakan sewaktu waktu untuk menunjang kegiatan HR di suatu perusahaan.</p>
                    <p>Dari jenis-jenis software HRIS yang sudah disebutkan di atas, Anda dapat memilih jenis HRIS yang paling sesuai dengan kebutuhan perusahaan Anda. Dokodemo-Kerja sebagai aplikasi HRIS terbaik di Indonesia menyediakan berbagai fitur yang berguna untuk pengelolaan SDM di perusahaan Anda. Dokodemo-Kerja memiliki fitur e-absensi, pengajuan cuti, pengelola keterlambatan karyawan, dan masih banyak lagi. Untuk informasi selengkapnya mengenai fitur-fitur Dokodemo-kerja, Anda dapat mengunjungi Halaman Fitur Dokodemo-Kerja.</p>

