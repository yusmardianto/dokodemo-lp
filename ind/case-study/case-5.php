                                    <h5>Dokodemo-Kerja</h5>
                                    <p>Dokodemo-Kerja merupakan HR <i>tools</i> yang dikembangkan oleh PT. LOGIQUE Digital Indonesia. Aplikasi ini memudahkan karyawan untuk bekerja lebih fleksibel dan menjadi lebih produktif walaupun bekerja dengan sistem <i>work from home</i>. Fitur-fitur yang ditawarkan oleh Dokodemo-Kerja sebagai berikut:</p>

                                    <ul>
                                        <li><i>Screen Record</i> atau perekam layar</li>
                                        <li>Pemantau waktu kerja tiap karyawan</li>
                                        <li>Pengelolaan karyawan, proyek, dan departemen</li>
                                        <li>Pelaporan waktu kerja yang tepat</li>
                                        <li><i>“Start”</i> dan <i>“Stop”</i> aplikasi dengan mudah</li>
                                        <li>Pemantauan karyawan dengan variabel objektif</li>
                                        <li>Request fitur tanpa penambahan biaya</li>
                                        <li>Aman karena <i>environment</i> dan server untuk setiap perusahaan dibedakan</li>
                                        <li>Aplikasi mobile yang <i>lightweight</i> (1.3 MB/hour)</li>
                                        <li>Tersedia di Windows, iOS, Linux, dan Android</li>
