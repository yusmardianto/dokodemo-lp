                                <div class="col-lg-7">
                                    <p>Halo, nama saya Ghitta, saya mulai bekerja sebagai staf HR sejak tahun 2017. Pada kesempatan kali ini saya ingin memperkenalkan bagaimana cara memanfaatkan “Dokodemo-Kerja” untuk membantu merubah peraturan sekaligus memecahkan masalah HR.</p>
                                    
                                    <div class="mb-4">
                                        <h5 class="mb-1"><b>Tahun 2017</b></h5>
                                        <p>Pada saat itu tidak ada peraturan HR yang spesial. Kalaupun ada pengecualian atau peraturan khusus adalah dimana sebagian kecil staf yang memiliki alasan khusus diijinkan untuk remote working.</p>
                                        <p>Oleh karena itu, sebagian besar staf yang bekerja di kantor pusat di Jakarta menentukan sendiri jam mulai bekerja mereka dari antara jam 8 sampai 10 pagi, dan staff juga harus bekerja selama 8 jam tidak termasuk 1 jam waktu istirahat. Selain itu, staf kantor Yogyakarta  mempunyai waktu  kerja dari jam 8 pagi sampai jam 5 sore.</p>
                                    </div>
                                </div>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>Tantangan</b></h5>
                                <p>Sebagian besar staf yang bekerja di Jakarta memulai pekerjaan mereka sekitar pukul 10:00. Di sisi lain, Yogyakarta dimulai dari pukul 8:00 pagi. Untuk kelancaran kerja sama kedua belah pihak, saya meminta mereka untuk mulai bekerja pada jam 10:00 pagi, akan tetapi hal itu tidak dapat terwujud karena banyak suara dari staf Yogyakarta yang ingin pulang lebih awal.</p> 
                                <p>Masalah lain yang terjadi pada kantor Jakarta adalah sulitnya mengontrol pekerjaan setelah staf HR dan staf tingkat leader sudah pulang setelah pukul 18:00. Setelah pukul 18:00, produktivitas pekerjaan pun tidak bisa dibilang tinggi karena banyak karyawan yang mulai berdoa, selain itu staf yang sudah menyelesaikan pekerjaan menjadi sering mengobrol dan makan dengan mereka yang sedang bekerja. Staf yang bekerja dari rumah juga di luar kendali manajemen. Satu-satunya hal yang bisa saya lakukan hanya setelah ada keterlambatan pada suatu tugas , adalah memeriksa gaya kerja seperti apa yang telah mereka kerjakan sampai waktu itu dan melakukan perbaikan.</p>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>Perbaikan</b></h5>
                                <p>"Dokodemo-Kerja" dikembangkan dan diperkenalkan untuk menyelesaikan berbagai macam masalah yang ada. Untuk pertama kalinya, kami dapat memvisualisasikan siapa yang bekerja, kapan, dan bagaimana cara mereka bekerja.</p>
                                <p>Setelah Dokodemo-Kerja diperkenalkan, staf-staf yang suka datang terlambat pun secara bertahap mulai mempunyai pola pikir "cepat datang berarti cepat pulang". Dengan adanya hasil rekaman jam kerja dengan Dokodemo-Kerja, para staf pun mulai menyadari bahwa dengan pola kerja mereka saat ini (termasuk pola istirahat), jam kerja yang sebenarnya itu masih kurang dari 8 jam walaupun mereka bekerja sampai pukul 21:00.</p>
                                <p>Sebagian besar staf pun mulai bekerja keras tanpa mengambil istirahat terlalu lama, dan kembali pulang sesegera mungkin. Pada saat yang sama, variasi saat mulai jam kerja mulai berkurang.</p>
                                <p>Kami sekarang dapat mengevaluasi staf yang bekerja dari rumah dengan lebih tepat. Untuk para remote worker yang selama ini tidak dapat memberikan hasil yang memuaskan, ternyata hal itu disebabkan oleh jam kerja mereka yang tidak teratur dan menjadi jelas bahwa jumlah pekerjaan yang mereka selesaikan pun tidak banyak.</p>
                                <p>Walaupun efek dengan diperkenalkannya Dokodemo-Kerja sudah dapat dilihat dan dirasakan, diperlukan sebuah cara pemantauan yang lebih efektif. Karena itu, dari bagian HR, ditugaskan satu staf khusus untuk memeriksa data Dokodemo-Kerja semua karyawan.</p>
                                <ol class="story__list">Ada empat aturan untuk pemonitoran Dokodemo-Kerja yang kami terapkan:
                                    <li>Ketika ada 3 screenshot menunjukkan gambar yang sama maka dikonfirmasi ke karyawan yang bersangkutan (untuk kantor Jakarta hal tersebut dikonfirmasikan secara tatap muka, jika tidak bisa maka dikonfirmasi melalui aplikasi komunikasi lain)</li>
                                    <li>Jika ada karyawan yang belum mulai bekerja walaupun sudah lewat dari jam 10:00 pagi, staff HR akan menanyakan langsung kenapa karyawan belum memulai Dokodemo-Kerja (untuk kantor pusat). Terkadang, walaupun sudah di kantor, karyawan sedang mengerjakan hal-hal yang bersifat pribadi seperti membuka media sosial. Untuk karyawan yang tidak berada di Jakarta, maka bisa dikonfirmasi melalui aplikasi komunikasi.</li>
                                    <li>Pada saat waktu makan siang,  sering kali aplikasi  tetap berjalan karena karyawan lupa memberhentikan. Oleh karena itu, sejarah screenshot harus dicek secara rutin.</li>
                                    <li>Di saat ada hasil screenshot game, youtube, media sosial, ataupun online shopping, maka harus diingatkan secara halus. Hal itu sendiri tidak menjadi masalah akan tetapi tidak termasuk dalam pekerjaan maka diwajibkan untuk menyetop aplikasi Dokodemo-Kerja.</li>
                                </ol>
                                <p>Selain itu, kami juga menempelkan selebaran di pintu masuk ruang ibadah yang menghimbau karyawan untuk tidak lupa memberhentikan Dokodemo-Kerja, dan tidak lupa melakukan pengecekan saat mulai dan selesai bekerja.</p>
                            </div>

                            <div class="row">
                                <div class="col-lg-7">
                                    <h5 class="mb-1"><b>Tantangan (Pertengahan 2018)</b></h5>
                                    <p>Walaupun sudah banyak karyawan yang mengubah cara kerja mereka menurut kebijakan yang telah dijelaskan di atas, akan tetapi masih ada beberapa karyawan yang tidak dapat mengikuti aturan. Misalnya, karyawan yang mengabaikan aturan penggunaan Dokodemo-Kerja dengan benar, dan ketika dikonfirmasi oleh HR mereka membalas dengan sombong.</p> 
                                    <p>Kalau hal ini terjadi pada karyawan yang produktif memang menjadi hal yang mengkhawatirkan, tetapi dalam kasus perusahaan kami, karyawan yang menunjukkan respon negatif merupakan karyawan yang tidak menunjukkan hasil, oleh karena itu saya memutuskan untuk memberikan evaluasi yang tegas. Sebagian karena efek itu, secara otomatis pergantian personel pun berjalan.</p> 
                                </div>
                                <div class="col-lg-5 offset-lg-0 offset-sm-2 col-sm-8 mb-lg-0 mb-3 align-self-center">
                                    <img src="/img/case-study/dokodemo-story-4.2-ghitta.png" alt="Logique Employees" class="img-fluid w-100">
                                </div>
                            </div>

                            <div class="mb-4">
                                <p>Selain itu, ada tantangan baru yang muncul di organisasi. Dengan adanya Dokodemo-Kerja, para karyawan pun dapat memanfaatkan program tersebut untuk memastikan tercapainya aturan jam kerja “8 jam per hari”. Tentu saja jumlah pekerjaan setiap karyawan itu bervariasi dan terkadang mereka tidak mendapatkan banyak tugas. Akan tetapi di saat mereka kekurangan tugas, mulai bermunculan karyawan yang terus menatapi layar Dokodemo-Kerja, menunggu untuk memenuhi 8 jam kerja.</p>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>Perbaikan</b></h5>
                                <p>Dari sisi perusahaan, lebih baik bagi karyawan untuk memiliki fleksibilitas dalam pengaturan jam kerja. Karyawan diharapkan untuk bekerja selama delapan jam atau lebih pada hari dimana ada pekerjaan yang mendesak, sehingga peraturan bekerja pun kami perbarui menjadi 40 jam kerja dalam seminggu, dan waktu kerja minimum dalam sehari adalah 6 jam ". Ditambah lagi, waktu kerja inti diterapkan dari 10:00 hingga 16:00. Sebenarnya, saya ingin lebih mempersingkat waktu inti kerja, tetapi karena ada pertimbangan kerjasama antar tim dan biaya manajemen, akhirnya ditetapkan pada 6 jam.</p> 
                                <p>Kami pun menambahkan beberapa poin pada SOP kami dimana staf yang bertanggung jawab untuk monitor Dokodemo-Kerja memeriksa apakah ada staf yang Dokodemo-Kerjanya tidak berjalan saat di waktu kerja inti, dan apakah ada staf yang dalam 1 minggu jam kerja nya belum mencapai 40 jam kerja.</p> 
                                <p>Untuk staf manajemen yang sering kali sibuk pada awal dan akhir bulan, pun mulai terbiasa dengan gaya kerja yang pulang di pukul 16:00 saat di tengah-tengah bulan. Bagi karyawan yang mengikuti kelas malam, ataupun mempunyai hal khusus di pagi atau malam karena keadaan keluarga, dapat terus bekerja secara efisien tanpa mengorbankan karir mereka.</p>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>Tantangan (Awal 2019)</b></h5>
                                <p>Kebijakan "40 jam per minggu" di atas telah meningkatkan produktivitas secara signifikan, akan tetapi saya mulai merasa bahwa harus ada peraturan yang lebih fleksibel. Adapun dua kebijakan itu adalah "memperbolehkan semua orang untuk bekerja dari jarak jauh" dan "memperpendek waktu inti". Alasannya adalah sebagai berikut.</p>
                                <ol class="story__list">
                                    <li>Lebih menguntungkan untuk karyawan yang tinggal jauh dari kantor. Biasanya karyawan harus menempuh perjalanan yang jauh dan mengalami berbagai kesulitan, dan setelah baru saja bekerja selama 6 jam, mereka sudah harus pulang untuk menghindari kemacetan.</li>
                                    <li>Banyak yang mengatakan bahwa setelah mereka selesai bekerja pada jam 16:00, sulit bagi mereka untuk memanfaatkan waktu yang tersisa secara efektif (karena mereka ingin mengakhiri pekerjaan sedikit lebih awal agar bisa memanfaatkan waktu pribadi pada malam hari).</li>
                                    <li>Untuk memenuhi persyaratan “40 jam per minggu”, ada beberapa kasus dimana karyawan kemudian bekerja di akhir pekan. Akan tetapi jam kerja yang singkat itu bukanlah cara yang efektif untuk bekerja.</li>
                                </ol>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>Perbaikan</b></h5>
                                <p>Pertama, kami mengizinkan hampir semua orang untuk melakukan remote working. Namun, jatah hari dalam satu minggu untuk remote working ditentukan berdasarkan jenis pekerjaan dan  situasi mereka. Awalnya, sebagian besar karyawan hanya diizinkan satu hari dalam seminggu untuk remote working.</p>
                                <p>* Pada tahap ini staf back office seperti GA dan HR tidak diizinkan untuk remote working. Agar tidak menurunkan tingkat kerjasama tim, pada hari remote working itu, hanya boleh dilakukan oleh karyawan yang telah diizinkan untuk melakukan remote working pada hari itu. Misalnya, "A mendapatkan hari Selasa, B mendapatkan hari Rabu dan Jumat”.</p>
                                <p>Tentu saja ada pengecualian jika terdapat jadwal rapat dengan mitra ataupun klien pada hari yang sama. Sebagai gantinya, waktu inti bekerja pun disesuaikan menjadi sedikit lebih pendek, dan diubah menjadi dari pukul 10:00 hingga 15:00. Selain itu, diadakan juga rapat khusus untuk memeriksa dan menyesuaikan jadwal remote working setiap orang.</p>
                                <p>Pengenalan sistem remote working untuk hampir semua karyawan sangat berkontribusi pada peningkatan produktivitas. Karyawan yang biasanya kelelahan karena harus bepergian ke kantor dapat menyelesaikan lebih banyak pekerjaan hanya dengan mengurangi perjalanan yang harus mereka tempuh selama 1-2 hari seminggu.</p>
                                <p>Karena sistem remote working telah menjadi hal yang wajar pada lingkungan kerja kantor dan saya pun menjadi lebih percaya diri terhadap pengelolaannya, sehingga saya dapat menambah jumlah karyawan yang bekerja dengan remote working secara penuh (misalnya karyawan yang tinggal di tempat terpencil / jauh).</p>
                            </div>
                            
                            <div class="mb-4">
                                <h5 class="mb-1"><b>Tantangan dan Peningkatan（Pertengahan 2019 sampai sekarang）</b></h5>
                                <p>Remote working memiliki efek positif, dan jumlah hari setiap karyawan dapat bekerja secara remote pun telah meningkat. Waktu inti kerja hari itu juga telah dikurangi menjadi 4 jam. Meskipun tidak ada masalah besar yang terjadi, masih ada beberapa masalah kecil yang mengharuskan penyesuaian aturan HR.</p> 
                                <p>Misalnya, jika ada karyawan yang terlambat bekerja di pagi hari, tim HR harus membuang waktu mereka untuk mendapatkan konfirmasi dari karyawan tersebut. Untuk mengatasi hal ini, jika terjadi keterlambatan 15 menit atau lebih dari waktu mulai kerja seharusnya, mereka diharuskan untuk memberitahu semua orang melalui Slack.</p> 
                                <p>Selain itu, karena pertemuan via telepon dan online meningkat, sering kali komputer atau laptop karyawan masuk ke status tidur/hibernasi dan aplikasi Dokodemo-Kerja secara otomatis berhenti berjalan. Walaupun Dokodemo-Kerja memiliki fitur pengeditan waktu, perlu diperjelas bahwa belum tentu semua pergantian waktu dapat diterima.</p> 
                                <p>Adapun tambahan peraturan yang lebih detil dimana tidak perlu mematikan atau menyetop aplikasi saat pergi ke toilet atau saat mengambil istirahat sejenak. Namun jika waktu istirahat tersebut melebihi 10 menit, maka aplikasi wajib dimatikan.</p> 
                                <p>Terlebih lagi, Dokodemo-Kerja juga sudah digunakan tidak hanya dalam LOGIQUE tetapi juga oleh pelanggan-pelanggan kami. Diperlukan penanganan yang lebih untuk berurusan dengan berbagai macam perusahaan dan sistem bisnis yang situasi kerjanya berbeda dari LOGIQUE. Contoh revisi besar yang telah kami lakukan adalah untuk mengakomodasi karyawan yang bekerja di lapangan sepanjang hari tanpa menggunakan laptop.</p> 
                                <p>Oleh karena itu, kami mengembangkan aplikasi mobile sehingga Dokodemo-Kerja dapat digunakan pada smartphone. Sekarang jadi memungkin untuk mengetahui waktu dan lokasi (GPS) dari staf sales saat mereka bekerja.</p> 
                                <p>Pada awal Maret 2020, telah ditetapkan kebijakan WFH penuh, yang menyebabkan kurangnya kesempatan di mana aplikasi mobile ini dapat digunakan secara efektif. Tetapi saya akan terus mengubah siklus PDCA dan mengoptimalkan peraturan HR.</p>
