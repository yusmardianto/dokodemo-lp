                                <p><b>Q: </b>Mengapa Anda berminat untuk menggunakan『Dokodemo-Kerja』?</p>
                                <p><b>A: </b>Perusahaan saya menyediakan berbagai layanan sistem penagihan perusahaan untuk berbagai macam perusahaan di Indonesia. Kami mempunyai kantor dengan atmosfer kerja multinasional, akan tetapi sejak jumlah karyawan melebihi 50 orang, kami merasa kesulitan untuk mengatur gaya atau cara kerja untuk masing-masing individu. <br>
                                Siapa yang bekerja secara efisien dan produktif, dan sebaliknya siapa yang tidak berkontribusi pada perusahaan? Saya pikir ada berbagai macam penyebab, salah satunya dari terus bertumbuhnya usaha dan jumlah <i>staff</i> tetapi tidak diseimbangi oleh strategi manajemen yang tepat, oleh karena itu pihak manajemen dan HR tidak dapat mengetahui jumlah output masing-masing karyawan, hal ini merupakan masalah yang penting untuk sebuah organisasi. <br>
                                Bahkan ketika memutuskan masalah yang penting seperti kenaikan gaji, tingkat kontribusi tidak dapat dievaluasi dengan benar, dimana saya harus menggunakan perhitungan berdasarkan sudah berapa lama karyawan itu bekerja, walaupun saya rasa kurang tepat. <br>
                                Tentu saja saya juga melihat pencapaian individu pada setiap proyek, akan tetapi tingkat kesulitan pada setiap proyek atau tugas akan bervariasi, sehingga akan terasa tidak adil untuk menilai berdasarkan hasil secara keseluruhan saja. Saya selalu ingin mengevaluasi tidak hanya dari hasilnya saja akan tetapi secara keseluruhan dari ide-ide, upaya individu dalam proses mencari solusi, cara mendekati pekerjaan, dan tingkat pertumbuhan individu selama proyek atau periode. <br>
                                Disaat itulah saya mengetahui tentang "Dokodemo-Kerja" yang dikembangkan oleh LOGIQUE. Kami memutuskan untuk menggunakannya di perusahaan kami, karena kami pikir itu bisa menjadi tools yang tepat untuk menyelesaikan masalah yang sedang dihadapi.
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>Apakah hasil yang didapat sesuai dengan ekspektasi？</p>
                                <p><b>A: </b>Melebihi ekspektasi saya. Hasil paling besar yang saya rasakan setelah menggunakan “Dokodemo-Kerja” adalah saya untuk pertama kalinya bisa mengetahui tipe bekerja untuk masing-masing karyawan. Sampai sekarang, saya terkejut melihat ternyata ada banyak karyawan yang memiliki karakter dan kepribadian yang benar-benar berbeda dari impresi yang saya dapatkan ketika berbicara langsung. <br>
                                Ada juga kejadian dimana seorang karyawan yang sibuk dengan pekerjaannya setiap hari dan sering kerja lembur, saya pun menjadi khawatir apakah saya telah memberikan tugas yang terlalu banyak. Namun, ternyata karyawan tersebut sering bekerja lembur karena memiliki cara kerja yang sangat tidak efisien dan sering menunda pekerjaan karena bermain <i>game online</i>. Hasilnya, saya bisa mendorongnya untuk merubah dan memperbaiki cara kerjanya. <br>
                                Tentu saja ada dampak positifnya juga, seperti walaupun <i>member</i> lain sedang bekerja lembur, ada satu karyawan yang pulang lebih dahulu, setelah diselidiki ternyata karyawan tersebut memperpendek jam istirahatnya agar bisa konsentrasi dalam bekerja dan pulang lebih cepat. <br>
                                Dengan cara ini, berbagai macam aspek yang sebelumnya tidak terlihat dapat dimasukkan dalam pembuatan penilaian yang komprehensif, dimana hasil evaluasinya dapat disetujui oleh semua pihak karena dilakukan secara adil dan objektif. Hasil dan efek yang dirasakan dari “Dokodemo-Kerja" untuk perusahaan sangatlah besar dan saya sendiri merasa puas.
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>Apakah dengan implementasi『Dokodemo-Kerja』juga mempunyai efek untuk sisi eksternal?</p>
                                <p><b>A: </b>Ya, seperti halnya pada perusahaan pengembangan sistem lainnya, biaya yang Anda bebankan kepada pelanggan Anda biasanya berdasarkan pada upaya kerja Anda. “Untuk proyek ini membutuhkan 10 orang / bulan jadi perkiraannya adalah $ X,XX". <br>
                                Walaupun jarang terjadi, saat berdiskusi dan tanda tangan kontrak dengan klien, tagihan kerja perusahaan kami memberikan keterangan bahwa kami sudah bekerja dengan 10 orang / bulan. <br>
                                Saat itu sangat umum bagi klien untuk mempertanyakan efisiensi kerja dan pengeluaran yang dibayarkan sudah sesuai atau belum. Hingga saat ini, tidak ada cara lain untuk membuktikan pekerjaan kecuali menyerahkan laporan "siapa dan siapa yang bekerja dan dari periode kapan ke kapan", tetapi berkat "Dokodemo-Kerja", jika ada permintaan dari klien, kami dapat menghitung dan memberikan bukti konkrit konten kerja yang sudah dilakukan. <br>
                                Dengan adanya referensi bukti yang jelas sangatlah berguna untuk membangun hubungan yang baik dengan klien, dan kami pun dapat dengan lebih percaya diri untuk melakukan atau menegosiasikan sebuah penawaran. <br>
                                Dan hal itu juga membantu ketika sistem yang kami kirim ke klien mengalami malfungsi. <br>
                                Setelah masalah itu terjadi, anda dapat memberikan laporan kepada klien dengan bukti bahwa pihak kami bekerja hingga tengah malam untuk pemulihan yang cepat. <br>
                                Di dalam laporan, saya dapat menuliskan dengan bukti dari “Dokodemo-Kerja” sistem seperti apa, dari jam berapa hingga siapa yang melakukan pekerjaan restorasi. Sebagai hasilnya, klien kami dapat lebih menghargai kerja keras kami, dan dapat membangun hubungan kerja yang lebih baik.
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>Terima kasih atas telah membagikan kepada kami pengalaman berharga Anda. Apakah ada pesan-pesan untuk pembaca lainnya?</p>
                                <p><b>A: </b>Biaya penggunaan “Dokodemo-Kerja” masih masuk akal dan saya pikir merupakan tool manajemen yang sangat baik. Saya pikir setiap perusahaan pasti memiliki masalah yang berbeda mengenai sumber daya manusia, tetapi jika perusahaan Anda mempunyai masalah yang sama seperti perusahaan kami dalam hal mengevaluasi pekerjaan atau permasalahan dengan tema yang serupa, saya rasa dengan menggunakan “Dokodemo-Kerja” dapat membuahkan sinergi yang baik. <br>
                                Saya berharap sebagai alat yang dapat meningkatkan produktifitas suatu organisasi dan keterampilan serta motivasi individu, dapat berperan untuk membangun cara kerja yang lebih baik. <br>
                                Dari pihak kami sendiri akan terus menggunakannya, Kami juga berharap LOGIQUE akan terus meningkatkan <i>tools</i> yang sudah ada.
                                </p>
                            </li>
