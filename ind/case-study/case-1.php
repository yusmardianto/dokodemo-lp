                        <div class="mt-3">
                            <p>Oleh karena itu di bulan November 2017 kami mengembangkan Dokodemo-Kerja. Dengan <i>tool</i> ini, kami dapat mengetahui kapan dan seperti apa karyawan bekerja (screenshot layar). Para <i>remote worker</i> pun akhirnya memiliki bukti bahwa mereka bekerja secara serius, ataupun disaat sebaliknya ketika harus menegur karyawan akan ada bukti yang jelas.</p>
                            <p>Pada fase ini, kami siap untuk memperluas implementasi <i>remote working</i>. Dari pertengahan tahun 2018, kami menerapkan sistem remote work kepada seluruh anggota kami. Tergantung dari tipe pekerjaan dan jabatan karyawan, kami memberikan ijin untuk melakukan <i>remote working</i> dari satu hari dalam seminggu sampai <i>full remote working</i>.</p>
                            <p>Sejalan dengan mulai terbiasanya terhadap budaya <i>remote working</i>, kami pun mulai melakukan penyesuaian terhadap cara kerja saat di kantor. Dokodemo-Kerja pun tidak hanya digunakan oleh para <i>remote worker</i>, tetapi mulai digunakan oleh karyawan yang berada di kantor. Oleh karena itu karyawan tidak harus berada di kantor dari pagi sampai malam. </p>
                            <p>Disaat tim diharuskan bertatap muka dahulu baru dapat bekerja, hal tersebut menjadi salah satu pengecualian. Setelah hal tersebut dicoba, kasus seperti ini tidak banyak. Selain itu para Karyawan dapat menyesuaikan cara kerja mereka dengan cara hidup mereka sehari-hari dengan menentukan jam mulai bekerja dan jam pulang masing-masing. </p>
                            <p>(Catatan 1.) Dengan syarat total waktu bekerja mencapai 40 jam dalam satu minggu dan ada hasilnya, mereka dapat bekerja dimana saja. Akan tetapi di waktu yang sama, bagi karyawan yang tidak dapat diukur hasil kerjanya seperti karyawan yang masih <i>junior level</i>, kami menilai dari cara penggunaan Dokodemo-Kerja, dan tingkat usaha mereka dalam bekerja. </p>
                            <p>Bagi karyawan yang tidak dapat memberikan hasil kerja yang memuaskan serta penggunaan Dokodemo-Kerja yang tidak baik, kami mulai memberikan disiplin karena mempunyai bukti yang cukup.</p>
                            <p>(Catatan 1) Jika waktu mulai bekerja tidak ditentukan dan dapat berubah, hal tersebut akan menurunkan tingkat efektivitas kerjasama tim. Oleh karena itu setiap karyawan dapat menentukan jam mulai bekerja mereka. Jika mereka akan telat dari jam yang sudah ditentukan, mereka harus memberitahukan kepada yang lain melalui Slack.</p>
                            <p>Dengan adanya peraturan dan skema <i>remote working</i> yang kami terapkan, mulai bermunculan berbagai macam cara bekerja.</p>
                            <p>Ada karyawan yang mulai bekerja di jam 9, makan siang bersama rekan kerja, kemudian pulang di jam 2 siang. Setelah melakukan pekerjaan rumah, kemudian di malam harinya kembali melakukan pekerjaan kantor.</p>
                            <p>Adapula yang mulai bekerja di rumah dari jam 6-8 pagi, kemudian datang ke kantor pada jam 10 tanpa makan siang dan terus bekerja hingga waktu pulang di jam 4 sore.</p>
                            <p>Karyawan yang tempat tinggalnya jauh dari tempat kerja pun ada yang <i>remote work</i> 3 hari dalam seminggu, dan di saat harus datang ke kantor, mereka bekerja dulu pagi harinya di rumah dan merubah jam keberangkatan agar menghindari macet. Dengan begitu mereka dapat mempunyai fokus, produktifitas, dan tingkat kebahagiaan mereka pun terlihat lebih baik.</p>
                            <p>Hal itupun tidak hanya terjadi di waktu datang dan pulang kerja, pada waktu mereka berada di kantor pun mengalami perubahan. </p>
                            <p>Disaat waktu selesai bekerja ditentukan, mau tidak mau jam tersebut menjadi dasarnya. Pada waktu tidak ada tugas, tidak enak badan pun harus tetap berada di kantor hingga jam selesai bekerja, efek pada karyawan pun bisa membuat sakitnya menjadi lebih parah ataupun tidak konsentrasi dalam bekerja dan hanya fokus pada jam pulang.</p>
                            <p>Hal-hal atau masalah yang sering terjadi di Indonesia itu ada beberapa seperti jam istirahat atau waktu beribadah yang panjang dan tidak kembali ke kantor ketika pergi merokok. Akan tetapi dengan menggunakan Dokodemo-Kerja para karyawan menjadi sadar dengan cara kerja masing-masing, dan jika mereka terus beristirahat maka mereka tidak bisa memenuhi peraturan dimana dalam seminggu bekerja 40 jam.</p>
                            <p>Ketika kesehatan sedang buruk atau sedang menghadapi masalah sehingga tidak bisa berkonsentrasi, mereka pun mendapatkan keuntungan dimana mereka dapat memutuskan sendiri tanpa harus melaporkan hal tersebut kepada HR ataupun atasan langsung. Dimana menurut saya dapat mendidik mereka untuk menumbuhkan percaya diri bekerja sebagai seorang yang profesional. </p>
                            <p>Untuk anggota HR sendiri bukan berarti dikurangi, tetapi sebaliknya kami tambahkan, jadi bagi karyawan yang membutuhkan konsultasi tetap bisa melakukannya.</p>
                            <p>Dengan cara ini, para karyawan mendapatkan kebebasan dalam bekerja sebagai profesional dan keseimbangan dalam kehidupan pribadi dan pekerjaan. Bagi LOGIQUE sendiri pun merasakan peningkatan dalam produktifitas, dan dari sisi HR maupun rekrutment pun mendapatkan hasil yang memuaskan. Sehingga seluruh proses ini menjadi situasi yang menguntungkan bagi kedua belah pihak.</p>
                            <p>Dari studi kasus LOGIQUE yang telah sukses menerapkan sistem ini, dan keinginan kami untuk berbagi dengan perusahaan lain di Indonesia, kami memutuskan untuk menjual Dokodemo-Kerja sebagai produk pada akhir tahun 2018.</p>
                            <p>Hingga saat ini pun kami masih menerima feedback untuk Dokodemo-Kerja dari pengguna lainnya sembari terus mengembangkan Dokodemo-Kerja. Kami juga terus memperbarui aturan kerja yang kami gunakan agar dapat membuat cara bekerja yang lebih baik lagi dan menguntungkan baik pihak karyawan maupun perusahaan.</p>
                            <p>Pada edisi selanjutnya kami akan memperkenalkan perusahaan lain yang menggunakan Dokodemo-Kerja.</p>
