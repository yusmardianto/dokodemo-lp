                            <p>Tetapi pada saat penerapan sistem <i>remote working</i>, terdapat reaksi yang kuat, terutama dari <i>team leader</i> bagian <i>Development</i>. Reaksi yang dirasakan itu adalah turunnya tingkat kerja sama tim, keluhan yang mengatakan bahwa sistem ini tidak adil (karena tidak perlu datang ke kantor). Oleh karena itu, ada perubahan pada cara penerapan dimana karyawan-karyawan yang memang <i>remote working</i> pun dapat dimengerti oleh karyawan lain, seperti karyawati yang baru melahirkan.</p>
                            <p>Masalah utama yang dirasakan yaitu sulitnya memvisualisasikan cara bekerja orang yang sedang <i>remote working</i> tersebut. Bagi perusahaan, memberikan kebijakan untuk memperbolehkan karyawan <i>remote working</i> merupakan suatu perlakuan yang spesial bagi karyawan tersebut, akan tetapi perusahaan selalu merasa tidak mendapatkan hasil yang sesuai. </p>
                            <p>Selain perusahaan merasa skeptis dengan tingkat konsentrasi dan etika kerja karyawan, dari karyawan lain pun mulai ada keluhan dari susahnya menghubungi satu sama lain ataupun lamanya waktu <i>offline</i>.</p>
                            <p>Akan tetapi ketika hal itu ditanyakan kepada karyawan yang bersangkutan, mereka menjawab sedang konsentrasi untuk memecahkan masalah yang sulit dan tidak melakukan hal yang lain. Ada juga yang menceritakan bahwa pada waktu <i>remote working</i> menjadi lebih sulit untuk mendapatkan bantuan dari anggota tim lain dan membutuhkan usaha yang lebih untuk menyelesaikan hal yang sama pada waktu bekerja di kantor. Dalam keadaan yang seperti itu sangatlah sulit untuk membedakan pihak mana yang benar, atau mungkin juga tidak ada pihak yang dapat dibilang benar.</p>
                            <p>Pokok permasalahannya adalah kalau kita tidak bisa memvisualisasikan para karyawan yang melakukan <i>remote working</i>, sistem tersebut tidak bisa memberikan nilai positif bagi para Karyawan maupun Perusahaan.</p>
