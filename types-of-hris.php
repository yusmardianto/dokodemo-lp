                    <p>HR solutions have provided a number of useful features; based on function, the HRIS software is divided into several types, namely:</p>

                    <h3 class="margintop-70 mb-4 font-weight-bold">1. Operational HRIS</h3>
                    <p>As the name implies, operational HRIS is specified to the needs of operational HR management tasks. This type of HRIS software aims to assist managers in regards to routine and day-to-day decision making protocol, as the system’s function is to provide data in regards to the management of human resources. Broadly speaking, some of the data provided includes:</p>
                    <ul>
                        <li class="mt-3">
                            <b>Employee Information Systems</b> <br>
                            This data consists of information regarding employee profiles such as their name, address, gender, nationality, length of service, and much more.
                        </li>
                        <li class="mt-3">
                            <b>Position Control Systems</b> <br>
                            Data relating to an employee’s position within a company. In general, the information provided by this database is useful for identifying any vacant positions that need to be filled immediately.
                        </li>
                        <li class="mt-3">
                            <b>Applicant Selection and Placement Information Systems</b> <br>
                            This data will be used by the HR team to support the candidate selection process in order to find prospective employees whose credentials are in accordance with what is required by the company.
                        </li>
                        <li class="mt-3">
                            <b>Performance Management Information Systems</b> <br>
                            This data relates to performance appraisals and assessing employee productivity. This data will usually be used to promote or to lay off employees when needed.
                        </li>
                        <li class="mt-3">
                            <b> Government Reporting and Compliance Information Systems</b> <br>
                            Data containing various information in order to maintain administrative order in accordance with government regulations.
                        </li>
                    </ul>

                    <h3 class="margintop-100 mb-4 font-weight-bold">2. Tactical HRIS</h3>
                    <p>Tactical HRIS is used to assist managers in making decisions regarding efficient resource allocation. This type of HRIS contains several sub-sections, namely:</p>
                    <ul>
                        <li class="mt-3">
                            <b>Job Analysis and Design Information Systems</b> <br>
                            Contains data relating to the duties, specifications and responsibilities of each employee.
                        </li>
                        <li class="mt-3">
                            <b>Recruiting Information Systems</b> <br>
                            Broadly speaking, this data is in regards to recruitment plans used in order to determine a position that needs to be filled, and the skills required for that position.
                        </li>
                        <li class="mt-3">
                            <b>Compensation and Benefits Information Systems</b> <br>
                            As the name implies, the data provided contains information that assists in the decision making process in regards to the provision of compensation and benefits to be obtained by employees.
                        </li>
                        <li class="mt-3">
                            <b>Employee Training and Development Systems</b> <br>
                            Information to assist the HR team in regards to directing training for willing employees or employees who would benefit from the training overall.
                        </li>
                    </ul>

                    <h3 class="margintop-100 mb-4 font-weight-bold">3. Strategic HRIS</h3>
                    <p>Strategic HRIS is a type of HRIS software that provides data for strategic decision making. Usually strategic HRIS will be used to manage labor negotiations, workforce arrangements and support certain HR programs.</p>
                    <p>By using a strategic HRIS, you can get an overview of your workforce planning and work assets. Strategic HRIS is comprised of several components, namely:</p>
                    <ul>
                        <li class="mt-3">
                            <b>Information Systems Supporting Workforce Planning </b> <br>
                            Contains information about the quantity and quality of labor owned by a company. This information can be used for long-term strategic planning, such as when a company wants to expand a new target market, open new branches, and so on.
                        </li>
                        <li class="mt-3">
                            <b>Information Systems Supporting Labor Negotiations</b> <br>
                            Contains a variety of information to support the labor negotiation process with vendors, trade unions, or other parties.
                        </li>
                        <li class="mt-3">
                            <b>Specialized Human Resource Information Systems Software</b> <br>
                            Supporting data for HR management.
                        </li>
                    </ul>

                    <h3 class="margintop-100 mb-4 font-weight-bold">4. Comprehensive HRIS</h3>
                    <p>Comprehensive HRIS makes up an integrated database that acts as a place that contains various information that can be used at any time to support HR activities in a company.</p>
