                    <h3 class="faq__q-title">一般</h3>

                    <h3 class="faq__q blue2">Q. Dokodemo-Kerjaはどのようなユーザーを想定して作られていますか？</h3>
                    <p>A. リモートワークを導入したい会社、社外のパートナー（フリーランサー）に時間給で作業を委託している会社、従業員の生産性の向上が課題となっている会社はすぐに効果を実感いただけます。他にもより柔軟性の高い働き方を可能とするHRポリシーを導入したいどんな会社にも有効なツールです。</p>
                    <h3 class="faq__q blue2">Q. インドネシア国外での使用も可能ですか？</h3>
                    <p>A. ご利用自体は国外でも問題ありません。異なるタイムゾーンでも管理可能です。ただし、ご利用料金は当社のインドネシアの銀行口座に送金いただく必要があります。</p>
                    <h3 class="faq__q blue2">Q. スマホでもDokodemo-Kerjaは動きますか？</h3>
                    <p>A. Dokodemo-Kerjaモバイルアプリをインストール済みのスマートフォンで動作します。外回りの営業スタッフや外での打ち合わせの多いスタッフのご利用を想定して作られています。勤務時間が記録＆管理できる以外に、GPSで位置情報を記録します。スクリーンショットの記録はしません。</p>
                    <h3 class="faq__q blue2">Q. 動作環境を教えてください</h3>
                    <p>A. Dokodemo-KerjaはWindows、Linux、Macで動作します。MobileアプリはAndroid版はご利用可能で、iOS版も近日中にご利用可能となります。（2020年7月予定）</p>
                    <h3 class="faq__q blue2">Q. 購入前に試用することは可能ですか？</h3>
