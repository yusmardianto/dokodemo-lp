                    <h3 class="faq__q-title">システム関連</h3>

                    <h3 class="faq__q blue2">Q. 勤務時間をどのように記録＆管理しますか？</h3>
                    <p>A. Dokodemo-Kerjaは従業員の勤務時間と選択されたタスクカテゴリを記録します。従業員がスタートボタンを押してからストップボタンを押すまでの時間を勤務時間とみなし記録します。その間ランダムにスクリーンショットが保存されるのでスタートしたけれど働いていないというケースも把握できます。<br>ストップボタンを押し忘れた場合にも、時間の編集機能を使用して修正することができます。編集したことはAdminユーザーに通知されます。</p>
                    <h3 class="faq__q blue2">Q. インターネットアクセスが必要ですか？</h3>
                    <p>A. はい。スタートボタンを押す際にはインターネットに接続されている必要があります。途中、短期間接続が切れても動作に影響ありません。</p>
                    <h3 class="faq__q blue2">Q. 途中でインターネット接続が切れた場合どうなりますか？</h3>
                    <p>A. 接続が切れても動作に影響ありません（スタート時のみが必須）。接続が回復したら「オフライン」での動作分がシステムに更新されます。太い（速い速度の）回線は必要ありません。</p>
                    <h3 class="faq__q blue2">Q. 何名のユーザーまで対応可能ですか？</h3>
                    <p>A. 上限はありません。</p>
                    <h3 class="faq__q blue2">Q. 異なるタイムゾーンで利用するユーザーがいても問題ありませんか？</h3>
                    <p>A. 問題ありません。自動でローカルタイムゾーンを把握して、その時間帯に基づいて労働時間を記録します。</p>
                    <h3 class="faq__q blue2">Q. Dokodemo-Kerjaを停止せずにコンピュータをシャットダウンしたりスリープ状態にした場合に何が起こりますか?</h3>
                    <p>A. こういった場合、その直前の最後にスクリーンショットが撮られ送信された時間までが勤務時間となります。スクリーンショットは最長10分間のうちにランダムに1回撮られますので、最長で10分間の勤務時間実績が失われることになってしまいます。</p>
                    <h3 class="faq__q blue2">Q. Dokodemo-Kerjaはバックグラウンドでも動作するのですか？</h3>
                    <p>A. はい。Dokodemo-Kerjaを終了すれば、常に動くサービスはありません。無駄にメモリーを消費するようなことはありませんのでご安心ください。</p>
                    <h3 class="faq__q blue2">Q. MacのCatalinaにも対応していますか</h3>
                    <p>A. はい、Catalina上でも動作します。ただし、OSをアップグレードした後、設定よりDokodemo-Kerjaがスクリーンを記録することを許可する必要があります。</p>
                    <h3 class="faq__q blue2">Q. Windows 7上でも動きますか?</h3>
                    <p>A. はい、Windows 7でも問題なく動きます。</p>
                    <h3 class="faq__q blue2">Q. 記録されたスクリーン画像はどこに保存されるのですか？</h3>
                    <p>A. LOGIQUEが管理するクラウドサーバー上に保存されます。アカウント毎に適切に分けて管理していますので、セキュリティ面でも安心ですし、他のアカウントの管理者やLOGIQUEが保存された画像を見ることはできません。</p>
