                    <blockquote class="blockquote text-center mb-5">
                        <p class="mb-0">“Work Forceの最適化ができたのは『Dokodemo-Kerja』のおかげです。”</p>
                        <div class="blockquote-footer"><cite title="Source Title">Operation Director</cite></div>
                    </blockquote>
                        <ul class="list-unstyled story__q-a">
                            <li>
                                <p><b>Q: </b>『Dokodemo-Kerja』を導入した経緯を教えてください。</p>
                                <p><b>A: </b>当社はクライアント企業のAccounting業務を代行、コンサルティングしています。１００社を超えるクライアントがいますが、サービスの性質上、月末月初やTHR（インドネシアにおけるボーナスのようなもの）支給時、年末や４月（年間課税額の決定）などが繁忙期です。逆に、通常月の月中は閑散期となります。<br>
                                とはいえ、月末月初のWork Load（業務量）に合わせてリソースを確保する必要があるため、月中はどうしても人員に余剰が生じていました。それでも、月末月初には多くの社員に残業が必要になる業務が発生するため、残業代の支払いも大きくならざるをえません。<br>
                                業態として、業務の繁閑期に合わせた人員調整が難しいけれど、もう少し業務量と残業材のバランスを取れないものか、と悩んでいた時、LOGIQUE社では、１日の労働時間ではなく、「週40時間制」として効率的に生産性を高めるワークスタイルを導入していると聞いたのです。<br>
                                LOGIQUE社のように当社でも、より柔軟な就業規則を適用することで、ピークシフトコントロールを効果的に行えるのではないかと考えました。<br>
                                まずは、１日8時間の就業時間のところ、コアタイムを６時間として、残りの２時間を繁忙期にAllocateするように就業規則を変更。今も日々就業規則を見直して最適化しています。<br>
                                こういった業務時間の最適化をするのに『Dokodemo-Kerja』は欠かせないツールとなっています。
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>社員からの反発はありませんでしたか？</p>
                                <p><b>A: </b>残業代が今までより減るのでは、と思いネガティブな反応をした社員はいました。しかし、彼らにとっても、業務がない時間にオフィスにいるという無駄な時間の過ごし方がなくなり、家族や友人との交流や趣味に時間を費やすことができる。また、月間スケジュールを計画的に設計できる、などのメリットがあることを理解してもらいました。<br>
                                個人が、それぞれのライフスタイルやワークスタイルに合わせた働き方ができるという、この仕組みのメリットに気付くのに比例して、満足度も向上。皆、以前よりモチベーション高く働いています。そういったオフィス環境の改善が口コミで広がったのか、同業他社からのJob Applicationも増えています。嬉しい相乗効果ですね。
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>働き方の新しいルールを適用する際のポイントは何だと思いますか？</p>
                                <p><b>A: </b>新しいルールへの理解や仕組みに慣れていくスピードには、個人差があります。理解が他の社員より遅い社員や、勤務スタイルを変えられない、もしくは変えようとしない社員には、繰り返し丁寧に説明することが肝要です。<br>
                                習慣化するまでは、「使いたくない」とか「難しい」、といったいろいろなネガティブな意見もありますが、一度習慣化すると、それまでの反発がなかったかのように、誰もが問題なく活用してくれます。最初のハードルは、粘り強く超えなければいけないと思います。<br>
                                あとは、一気に変更するのではなく、段階的に適用していくということと、それを周知することも大事だと思います。経営層やHRが一方的に押し付けるプランではなく、組織全体でコミュニケーションを取りながら最適化していくのだ、ということを分かってもらうということです。結果をみながらルールを最適な形に変えていくということが分かれば、社員からもポジティブで生産的なフィードバックが得られます。<br>
                                わが社でも、その重要な下地作りの分析のために、『Dokodemo-Kerja』を使う必要がある、ということは繰り返し説明しました。
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>導入いただいてから１年ほどになりますが、導入直後と現在で変わったことはありますか？</p>
                                <p><b>A: </b>導入後1年が経ちますが、働き方を変えたことによるポジティブな効果は続いています。引き続き社員たちをモニターしながらルールを調整していますので、１年経っても変わらず有効活用できていると思います。社員の入れ替わりもありますが、使い方に大差はないため、どういった社員が入社してもフレキシブルに活用できるのが良いですね。<br>
                                そうそう。個人の裁量が大きい営業職の社員には『Dokodemo-Kerja』を使っていなかったのですが、先日モバイルAppが発売されたので、近いうちに試してみたいと考えています。
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>なるほど。現在は主に、会計コンサルティング業務を行っている社員が『Dokodemo-Kerja』を利用しているのですね？</p>
                                <p><b>A: </b>そうですね。コンサルタント職と事務職合計約60名のコンピューターにインストールして利用しています。HR部は管理者として３名が活用しています。HR部ではそれぞれの担当を決め、『Dokodemo-Kerja』を通して、毎日、コンサルタントと事務職スタッフが生産的に業務を遂行しているかを管理しています。
                                </p>
                            </li>
                            <li>
                                <p><b>Q: </b>HR部からのフィードバックはいかがですか？</p>
                                <p><b>A: </b>こちらの部署でも、最初はネガティブな反応もありましたが、習慣化した後は前向きな意見だけになりました（笑）。<br>
                                最所は、既存業務だけで手一杯で、『Dokodemo-Kerja』を使って社員の働きをチェックする、といった業務時間はとてもではないが捻出できない、などという意見がありましたが、『Dokodemo-Kerja』導入の効果で、労働時間の集計や証拠との突き合わせといったHRの既存の業務量が、まずは削減できたことで風向きは変わりましたね。<br>
                                『Dokodemo-Kerja』が抑止力となって、許容できない長さの休憩や、業務中の仕事とは関係のない作業をしている社員が激減したことで、結果として、HRも本来やるべき生産性の高い業務を行うことができています。HR部の組織力も向上したと思います。
                                </p>
                            </li>
                            <li>貴重なご意見をいただき、有難うございました。</li>
