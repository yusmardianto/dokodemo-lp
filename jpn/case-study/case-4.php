                                    <img src="/img/case-study/dokodemo-story-4-ghitta.jpg" alt="A Logique Employee" class="img-fluid w-100 mb-4">
                                </div>
                                <div class="col-lg-7">
                                    <p>HRスタッフのGhittaです。2017年からHRスタッフとして働いています。今回は、「Dokodemo-Kerja」をどのように活用して人事ルールを改変し問題解決してきたかについてご紹介します。</p>
                                    
                                    <div class="mb-4">
                                        <h5 class="mb-1"><b>2017年当時</b></h5>
                                        <p>当時は、特別な人事規則はありませんでした。特例として、特別な理由のある一部のスタッフにのみ、リモートワークを許可する、という環境でした。</p>
                                        <p>ですので、本社勤務の大多数のスタッフは、朝８時から10時の間で勤務開始時刻を自ら設定。開始時間からお昼の休憩時間（１時間）を除いて８時間働くという条件になっていました。なお、ジョグジャカルタオフィスのスタッフは、朝８時から17時までの勤務形態になっていました。</p>
                                    </div>
                                </div>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>課題</b></h5>
                                <p>ジャカルタで働くスタッフの多くは、勤務スタートが10時近くからでした。一方、ジョグジャカルタは８時から。双方の連携のためにも、彼らに１０時から勤務開始を持ちかけましたが、ジョグジャカルタのスタッフの、「早く帰りたい」という理由から実現しませんでした。</p>
                                <p>ジャカルタオフィスでは、１８時以降、HRスタッフやリーダーレベルの社員が帰った後、業務のコントロールができないことも課題でした。１８時以降はマグリブのお祈りをしたり、仕事を終えたスタッフと勤務中のスタッフが関係なく食事をしたりおしゃべりをしたりと、生産性が高いとはいえない状況でした。</p>
                                <p>在宅勤務のスタッフもコントロールできていませんでした。期日に遅れるなどして初めて、それまでの期間どういう働き方をしていたのか確認し改善させる、という対応しかできていませんでした。</p>
                            </div>


                            <div class="mb-4">
                                <h5 class="mb-1"><b>改善</b></h5>
                                <p>こういったさまざまな問題がある中、「Dokodemo-Kerja」は開発・導入されました。そして、まずは誰がいつどのように働いているのかを可視化しました。</p>
                                <p>出退勤が遅かったスタッフ達は、Dokodemo-Kerjaの導入後、少しずつ、『早く来て早く帰る』ようになりました。Dokodemo-Kerjaで記録してみると、それまでの働き方（休憩の取り方）では、21時まで勤務していた場合でも、実質の労働時間は８時間に満たない、ということに気づいたからです。</p>
                                <p>誰もが、過度に長い休憩を取らずに集中して働き、なるべく早く帰るようになりました。それと同時に、勤務時間のバラつきも減少したのです。</p>
                                <p>在宅勤務しているスタッフの評価も適切に行うことができるようになりました。結果が出ていないリモートワーカーは、やはり生活（勤務）時間に乱れが見られ、こなした仕事の量は少ないことが明らかになりました。</p>
                                <p>既に、Dokodemo-Kerjaの導入効果に自信を深めていましたが、さらに正しくモニターするために、HR部で１人専任スタッフをアサインし、このスタッフは、全社員のDokodemo-Kerjaを常にチェックすることとしました。</p>
                                <ol class="story__list">監視ルールは、以下の4点です。
                                    <li>３枚同じスクリーンショットが並んだら、本人に確認する（ジャカルタオフィスの場合は目視確認可能、そうでない場合はチャットツールで勤務中か確認）</li>
                                    <li>午前10時を過ぎても勤務を開始していないスタッフがいた場合、ジャカルタオフィスはDokodemo-Kerjaで開始できない理由を確認。自席にいても、ソーシャルメディアを確認するなどプライベートなことをしている場合は、就業時間を過ぎたので仕事を始めるようにこまめに声掛けをする。ジャカルタオフィス以外は本人にメッセージを送信。</li>
                                    <li>昼食時間に記録が停止することなく続いている場合は、ストップボタンの押し忘れである可能性が高いので都度確認する。</li>
                                    <li>ゲームやYoutube、ソーシャルメディア、ショッピング画面が記録された場合は、ソフトに警告する。（閲覧するのは構わないが、その際は就業時間としてはカウントをしないため、Dokodemo-Kerjaの記録をストップするように伝える）。</li>
                                </ol>
                                <p>他にも、お祈り部屋に、『休憩時はDokodemo-Kerjaをストップするのを忘れない』などの張り紙をするなどして、勤務の開始・終了時ともチェックを忘れないよう啓蒙しました。</p>
                            </div>

                            <div class="row">
                                <div class="col-lg-7">
                                    <div class="">
                                        <h5 class="mb-1"><b>課題（2018年半ば）</b></h5>
                                        <p>前述のような活動を重ねる中で、多くの社員の勤務態度は大きく変わりましたが、それでもそのルールにはまらない社員が少数いました。たとえば、きちんとDokodemo-Kerjaを使用するというルールを無視する社員、HRスタッフからの確認に対して慇懃な態度で対応する社員などです。</p>
                                        <p>非常に生産性の高い社員でこういうことがあると悩ましいところですが、当社の場合は、このような態度を取るのは結果を出していない社員でしたので、厳しい評価をすることに決定。その効果もあって、自然と、人員の代謝が進みました。</p>
                                    </div>
                                </div>
                                <div class="col-lg-5 offset-lg-0 offset-sm-2 col-sm-8 mb-lg-0 mb-3">
                                    <img src="/img/case-study/dokodemo-story-4.2-ghitta.png" alt="Logique Employees" class="img-fluid w-100">
                                </div>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>それ以外にも、組織全体として新たな課題がでました。</b></h5>
                                <p>『１日８時間勤務』というルール、その勤務時間をDokodemo-Kerjaを通して自分でも確認できることから、システム上の時間に囚われる社員がでてきました。もちろん、日によって業務量は異なるのですが、タスクが少ないときでも、Dokodemo-Kerjaを眺めて、「早く８時間にならないかなぁ」と待っているスタッフが出てきたのです。</p>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>改善</b></h5>
                                <p>会社としても、社員には、緊急性の高い業務がある日に８時間以上勤務してもらえる柔軟性があったほうが良いので、就業規則を改訂。『週に40時間の勤務、１日の最低勤務時間を６時間』としました。プラス、コアタイムは10時から16時に設定。本当は、コアタイムはさらに短くしたかったのですが、チームワークが多い部署やHRの管理コストを考慮し決定しました。</p>
                                <p>Dokodemo-Kerjaをモニターする担当スタッフは、コアタイムにDokodemo-Kerjaが止まっているスタッフがいないかチェックする、週に40時間に満たなかったスタッフをチェックするという確認項目がSOPに追加されました。</p>
                                <p>管理スタッフは、月末月初は多忙なので月中は１６時過ぎたら帰宅するなどの働き方が定着しました。</p>
                                <p>夜間学校に通学している社員や家庭の事情で朝や夜に用事がある社員が、キャリアを犠牲にせず効率的に働き続けることができるようになりました。</p>
                            </div>
                            
                            <div class="mb-4">
                                <h5 class="mb-1"><b>課題③（2019年初）</b></h5>
                                <p>上記の『週40時間制』は生産性の向上に大きく寄与したのですが、更に柔軟なルールにすべきだと感じることが出てきました。方針としては、「全員にリモートワークを許可する」「コアタイムを短縮する」というこの2点です。この背景には以下のような理由があります。</p>
                                <ol class="story_list">
                                    <li>長距離通勤にの社員は大変な思いをして会社に着いた後、６時間勤務しただけで帰宅せざるをえない（その時間にはまた通勤ラッシュが始まる）、ということにメリットを見出しにくかった。</li>
                                    <li>16時に終業しても、その後の時間を有効活用することは、思いの外難しいという声が多かった（夕方をプライベートな時間として有効活用するにはもう少し早く終業する必要がある）。</li>
                                    <li>勤務時間が週40時間に満たなかった場合、その僅かな時間を取り返すために、週末に働くケースがでてきたが、そのような極端に短い勤務は効果的な働き方ではなかった。</li>
                                </ol>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>改善</b></h5>
                                <p>まずは、ほぼ全員にリモートワークを許可しました。ただし、職種やそれぞれの事情を鑑みて週に何日リモートワークを許可するかは個別に決定。最初は、ほとんどの社員は、週１日だけ許可されました。</p>
                                <p>※GAやHRなど、バックオフィススタッフはこの段階ではリモートワークは不可としました。</p>
                                <p>リモートワークになっても、チームワークが滞らないように、リモートワークをする日を各人が設定し、設定日以外の日にリモートワークをすることはできないということも取り決めました。たとえば、「Aさんは火曜日、Bさんは水曜日と金曜日」というように……。</p>
                                <p>また、オフィスでのミーティングは、各人のリモートワークのスケジュールを確認・調整することとなりました。もちろん必要性の高い場合や、クライアント・パートナーとのミーティングはリモートワーク設定より優先されるとも規定しました。</p>
                                <p>補足ですが、コアタイムも少しだけ短く、10時から15時に変更しました。</p>
                                <p>リモートワークのほぼ全社員への導入は、生産性の向上に大きく寄与しました。通勤で疲弊していた社員は、週に１～２日その労力が減るだけで、より多くの業務量をこなすようになりました。</p>
                                <p>リモートワークが当たり前のオフィス環境になり、それを管理できる自信もついたので、『フルリモートで働く社員（たとえば、遠隔地に住んでいるなど）』の採用も増やしました。</p>
                            </div>

                            <div class="mb-4">
                                <h5 class="mb-1"><b>課題と改善（2019年半ば以降）</b></h5>
                                <p>リモートワークはポジティブな効果をもたらし、社員それぞれのリモートワークする日は増加していきました。１日のコアタイムも４時間に短縮しました。大きな問題は生まれなかったとはい、細かい問題はまだありましたので、それに対応してHRルールを調整していきました。</p>
                                <p>たとえば、朝の始業時間に遅刻するなど問題があった場合、その社員への確認やヘルプが必要な際に待ち時間が発生してしまいます。これに対応するため、朝の始業時間は各自が設定し、15分以上遅れる場合にはSlackで皆にその旨を通知連絡することにしました。</p>
                                <p>また、電話ミーティングやオンラインミーティングが多くなり、ラップトップパソコンがスリープ状態になることで、Dokodemo-Kerjaの記録が停止されることも多くなりました。時間の編集機能は、Dokodemo-Kerjaにはあるのですが、この編集機能の使い方や編集時間の確認フローを、あらためて明確にしました。</p>
                                <p>なお、トイレ休憩は記録を停止する必要はありませんが、10分を超えるような休憩を取る際は停止する必要があるなど、細かいルールも都度追加していきました。</p>
                                <p>なにより、LOGIQUE社内だけでなく、お客様にもDokodemo-Kerjaをご利用いただくようになり、LOGIQUEとは勤務実態が異なる会社や業務体系にもさまざまに対応していく必要があることが分かってきました。特に大きな改訂だったのは、ラップトップなどを利用せずに、1日の多くの時間を現場などで働く社員への対応などです。</p>
                                <p>そこで、スマートフォンでもDokodemo-Kerjaを利用できるようにモバイルアプリを開発。営業スタッフなどがGPSで捕捉された場所に、何時までいたのかなどをタイムリーに把握できるようになりました。</p>
                                <p>2020年３月以降はWFHとなり、このモバイルアプリを有効活用できる状況はまだありませんが、またPDCAサイクルを回し、人事規則も最適化していこうと思います。</p>
                            </div>
        
